    private void AgregarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarButtonActionPerformed
        String etiqueta = EtiquetaTextField.getText();
        
        if(etiqueta.equals("")){
            JOptionPane.showMessageDialog(this, "La etiqueta no puede estar vacÃ­a", "Ingresar etiqueta", WARNING_MESSAGE);
        }else{
            boolean repetido = false;
            DefaultListModel modelo = (DefaultListModel) EtiquetasList.getModel();
            for (int i = 0; i < modelo.getSize(); i++) {
                String e = (String) modelo.getElementAt(i);
                if(e.equals(etiqueta)){
                    repetido=true;   
                    break;
                }
            }
            if(repetido){
                JOptionPane.showMessageDialog(this, "Ya ingresÃ³ la etiqueta '"+etiqueta+"'", "Etiqueta repetida", WARNING_MESSAGE);
            }else{
                modelo.addElement(etiqueta);
            }
        }
    }//GEN-LAST:event_AgregarButtonActionPerformed

    private void BorrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BorrarButtonActionPerformed
        if(EtiquetasList.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una etiqueta", "Seleccionar etiqueta", WARNING_MESSAGE);
        }else{
            DefaultListModel modelo = (DefaultListModel) EtiquetasList.getModel();
            modelo.removeElementAt(EtiquetasList.getSelectedIndex());
        }
    }//GEN-LAST:event_BorrarButtonActionPerformed

    private void CrearButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearButtonActionPerformed
        String titulo = TituloTextField.getText();
        String texto = TextoTextArea.getText();
        
        List etiquetas = new ArrayList();
        DefaultListModel modelo = (DefaultListModel) EtiquetasList.getModel();
        for (int i = 0; i < modelo.getSize(); i++) {
            String e = (String) modelo.getElementAt(i);
            etiquetas.add(e);
        }
        
        Fabrica.getInstance().getContAdmin().nuevaNoticia(titulo, texto, etiquetas);
    }//GEN-LAST:event_CrearButtonActionPerformed

    

